<?php

namespace App\Models\Admin;

use Eloquent as Model;
use Illuminate\Database\Eloquent\SoftDeletes;

/**
 * Class Roles
 * @package App\Models\Admin
 * @version August 29, 2020, 5:13 am UTC
 *
 * @property string $name
 * @property string $guard_name
